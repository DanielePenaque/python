#Operadores artimeticos

#Entrada
numero = float(input('Digite um número: '))

#Calculo
dobro = numero *2
triplo = numero *3

#Saida
print(f'O número é {numero}')
print('-'*40)
print(f'O dobro é: {dobro}')
print('-'*40)
print(f'O triplo é: {triplo}')

#Finalizando
print('='*40)
print('Fim do programa!')