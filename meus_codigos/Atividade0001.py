#Operadores aritméticos
#Soma e Multiplicação

#Entrada

valor_a = int(input('Digite o valor a: '))
valor_b = int(input('Digite o valor b: '))
valor_c = int(input('Digite o valor c: '))

#Soma
soma = valor_a + valor_b + valor_c
#Multiplicação
multiplicacao = valor_a * valor_b * valor_c

#Saida
print (f'O valor da soma é: {soma}')
print (f'A multiplicação dos valores é: {multiplicacao}')

#finalizando
print('='*40)
print('Fim do programa!')
