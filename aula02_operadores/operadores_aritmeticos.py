#Operadores artimeticos

#Declarações
a = 10
b = 2
#operações
#soma
soma = a + b
#Subtração
subtracao = a - b
#Produto
produto = a * b
#divisão
divisao = a / b
#Potência
potencia = a ** b
#Divisão inteira
divisao_inteira = a // b
#Resto da divisão
resto_divisao = a % b 

#Saída Formatada
print('-'*40)
print('ESTUDO DE OPERADORES ARITMÉTICOS')
print('='*40)
print(f'O valor de {a} + {b} = {soma}')
print(f'O valor de {a} - {b} = {subtracao}')
print(f'O valor de {a} * {b} = {produto}')
print(f'O valor de {a} / {b} = {divisao}')
print(f'O valor de {a} ** {b} = {potencia}')
print(f'O valor de {a} // {b} = {divisao_inteira}')
print(f'O valor de {a} % {b} = {resto_divisao}')
print()
